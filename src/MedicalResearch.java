    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.IMAGE", "jpg", "gif", "png");
        fileChooser.addChoosableFileFilter(filter);
        int result = fileChooser.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            s = path;
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("No Data");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        number = Integer.parseInt(jTextFieldPatient.getText());
        String inserSQLQuery = "INSERT INTO `Medical Research`(`Patient`, `Medical Research`) VALUES (?,?)";

        try {
            PreparedStatement statement = this.connx.prepareStatement(inserSQLQuery);
            InputStream is = new FileInputStream(new File(s));
            statement.setInt(1, number);
            statement.setBlob(2, is);
            int inserted = statement.executeUpdate();
            if (inserted > 0) {
                JOptionPane.showMessageDialog(null, "Inserted Successfully...");
                writeInTable();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MedicalResearch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int modelSelectedRow = jTable1.getSelectedRow();
        if (modelSelectedRow == -1) {
            JOptionPane.showMessageDialog(null, "No row selected");
        } else {
            try {
                ImageIcon ImageFromSelectedJtableRowIcon = new ImageIcon(patientslist().get(modelSelectedRow).getImage());
                Image ImageFromSelectedRowIconResized = ImageFromSelectedJtableRowIcon.getImage().getScaledInstance(jLabel.getWidth(), jLabel.getHeight(), Image.SCALE_SMOOTH);
                jLabel.setIcon(new ImageIcon(ImageFromSelectedRowIconResized));
            } catch (SQLException ex) {
                Logger.getLogger(MedicalResearch.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

