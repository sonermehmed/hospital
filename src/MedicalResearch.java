    public ArrayList<Patient> patientslist() {

        ArrayList<Patient> patientsList = new ArrayList<Patient>();
        String selectAllSQLQuery = "SELECT * FROM `medical research`";
        Statement st;
        ResultSet rs;

        try {
            st = connx.createStatement();
            rs = st.executeQuery(selectAllSQLQuery);

            while (rs.next()) {
                Patient patient = new Patient();
                patient.setNum(rs.getInt("Patient"));
                patient.setImage(rs.getBlob("Medical Research"));
                patientsList.add(patient);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }

        return patientsList;

    }

    public void writeInTable() throws SQLException {

        ArrayList<Patient> pt = patientslist();
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[2];

        for (int i = 0; i < pt.size(); i++) {
            if (Integer.parseInt(jTextFieldPatient.getText()) == pt.get(i).getNum()) {
                row[0] = pt.get(i).getNum();
                row[1] = pt.get(i).getImage();
                model.addRow(row);

            }
        }
    }

