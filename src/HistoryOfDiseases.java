    public ArrayList<Patient> patientslist() {

        ArrayList<Patient> patientsList = new ArrayList<Patient>();
        String selectAllSQLQuery = "SELECT * FROM `historyofdiseases`";
        Statement st;
        ResultSet rs;

        try {
            st = connx.createStatement();
            rs = st.executeQuery(selectAllSQLQuery);

            while (rs.next()) {
                Patient patient = new Patient();
                patient.setNum(rs.getInt("ID"));
                patient.setfirstName(rs.getString("Name"));
                patient.setDate(rs.getString("Date"));
                patient.setDiseases(rs.getString("Disease"));
                patient.setDoctor(rs.getString("Doctor"));
                patient.setTherapy(rs.getString("Therapy"));
                patientsList.add(patient);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }

        return patientsList;

    }

    public void writeInTable() throws SQLException {

        ArrayList<Patient> pt = patientslist();
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];

        for (int i = 0; i < pt.size(); i++) {
             if (Integer.parseInt(jTextFieldPatient.getText()) == pt.get(i).getNum()) {
            row[0] = pt.get(i).getNum();
            row[1] = pt.get(i).getfirstName();
            row[2] = pt.get(i).getDate();
            row[3] = pt.get(i).getDiseases();
            row[4] = pt.get(i).getDoctor();
            row[5] = pt.get(i).getTherapy();
            model.addRow(row);

            }
        }
    }

