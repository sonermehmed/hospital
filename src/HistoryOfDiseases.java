    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        num = Integer.parseInt(jTextFieldPatient.getText());
        firstName = jTextFieldfirstName.getText();

        java.util.Date date1 = jDateChooserDate.getDate();
        date = DateFormat.getDateInstance().format(date1);
        diseases = jTextFieldDiseases.getText();
        doctor = jTextFieldDoctor.getText();
        therapy = jTextFieldTherapy.getText();

        if (firstName.isEmpty() || date.isEmpty() || diseases.isEmpty() || doctor.isEmpty() || therapy.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Check if all fields are filled!!!");
        } else {

            String inserSQLQuery = "INSERT INTO `historyofdiseases`(`ID`, `Name`, `Date`, `Disease`, `Doctor`, `Therapy`) VALUES (?,?,?,?,?,?)";

            try {
                PreparedStatement statement = this.connx.prepareStatement(inserSQLQuery);
                statement.setInt(1, num);
                statement.setString(2, firstName);
                statement.setString(3, date);
                statement.setString(4, diseases);
                statement.setString(5, doctor);
                statement.setString(6, therapy);
                int inserted = statement.executeUpdate();
                if (inserted > 0) {
                    JOptionPane.showMessageDialog(null, "Inserted Successfully...");
                    clearAllInputFields();
                    writeInTable();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

